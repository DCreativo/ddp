          </p>

          <p class="h5 mb-0"><b><u>DÍA 5: AVENTURA KABUR (LAS REYNAS)</u></b></p>
          <p class="text-justify">
            Am: Desayuno<br>
            Am: Para empezar nuestro recorrido nos equiparemos con cuerdas, cascos y otros accesorios, partiremos hacia el poblado de Kimiri, cruzaremos el impresionante puente colgante Kimiri declarado patrimonio Cultural de la Nación, e iniciaremos una divertida caminata de dos horas aproximadamente a través de un sendero que nos conducirá hacia el rio Kabur, durante el trayecto observaremos hermosos paisajes con abundante vegetación selvática y algún ocasional animal, llegaremos al río e iniciaremos el camino de aventura río arriba, deberemos superar divertidos obstáculos que encontraremos en el trayecto; treparemos por lianas, escalaremos por paredes de roca, por cascadas, en todo momento nos refrescaremos y podremos nadar en las innumerables pozas de aguas cristalinas, luego de superar todos los obstáculos conoceremos las sorprendentes  cataratas llamadas Las tres Reynas, de prístino ambiente selvático; regresaremos por un camino paralelo por el frondoso bosque. <b><u>RECOMENDACIONES:</u></b> Leggins o shorts, polo, zapatillas (que puedan mojar) o aquashoes, una muda de cambio, toallas, gorra, lentes para sol, repelente y bloqueador solar.
          </p>

          <p class="h5 mb-0"><b><u>DÍA 6: VILLA RICA</u></b></p>
          <p class="text-justify">
            Desayuno. Partimos hacia Villa Rica y nos trasladaremos hacia una finca cafetalera para observar el cultivo de Café, dar un paseo por los bellos cafetales cubiertos por frondosos árboles oriundos del lugar u otros producto de la reforestación, después degustaremos una deliciosa taza de Café, luego visitaremos, El Oconal un bello humedal, donde disfrutaremos de un divertido paseo en canoas, en el mismo lugar se podrá hacer observación de aves Garzas, Cormoranes y muchas otras especies de ave, después nos trasladaremos hacia el centro de la ciudad para conocer su Plaza Principal, Iglesia Nuestra señora de Rosario, la cafetera gigante y poder degustar el café más fino del mundo; por la tarde conoceremos la bellísima catarata el León, donde podremos refrescarnos en sus pozas naturales, para finalizar nuestro recorrido visitaremos el Mirador, desde donde tendremos una bella vista panorámica de Villa Rica, “la Tierra del Café más Fino del Mundo”. <b><u>RECOMENDACIONES:</u></b> Ropa Ligera, zapatillas, ropa de baño, toallas, gorra, lentes para sol, repelente y bloqueador solar.
          </p>

