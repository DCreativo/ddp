            <b>11:00 a.m. </b>Tendremos la visita a la Reserva Nacional de Paracas, visitaremos la famosa <b>PLAYA LA MINA</b>, zona protegida con la finalidad de conservar y proteger las diversas especies de flora y fauna silvestres que allí viven, podremos observar aves como la parihuana o flamenco la cual sirvió al General San Martín como inspiración para la creación de los colores Patrios, también podremos observas otras especies migratorias o residentes. Almuerzo típico, tarde libre para disfrutar de la Bahía de Paracas.<br>
